#include "Port_init.h"
void Port_init()
{
		DDRC = 0xff;                      //порт на выход реле К1--К8
		PORTC = 0;                        //выключение порта
		DDRB |= (0 << PB5)|(0 << PB4)|(1<<PB7)|(1<<PB6);// Пины 5,4--ввод  7,6--выход
		PORTB |= (1 << PB5)|(1 << PB4)|(0<<PB7)|(0<<PB6);// на 5,4 пинах вкл. подтяжка +5в 7,6---отключено реле К9
}